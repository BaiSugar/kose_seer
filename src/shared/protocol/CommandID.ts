export type CommandIDType = typeof CommandID[keyof typeof CommandID];

// 命令ID到名称的映射
const _cmdNameMap = new Map<number, string>();
for (const [name, id] of Object.entries(CommandID)) {
  _cmdNameMap.set(id as number, name);
}

/**
 * 获取命令名称
 */
export function GetCommandName(cmdID: number): string {
  return _cmdNameMap.get(cmdID) || `UNKNOWN_${cmdID}`;
}
